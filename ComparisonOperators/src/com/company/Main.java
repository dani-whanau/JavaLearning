package com.company;

public class Main {

    public static void main(String[] args) {
        int x = 1;
        int y = 1;
        System.out.println(x == y); // Equal
        System.out.println(x != y); // NOT equal to
        System.out.println(x > y); // Greater than
        System.out.println(x >= y); // Greater or equal than
        System.out.println(x < y); // Less than
        System.out.println(x <= y); // Less or equal than
    }
}
